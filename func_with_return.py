def my_avg(num1, num2):
    """The function returns average of 2 integers"""
    avg: float = (num1 + num2) / 2
    return avg


def my_headline(str1):
    """The function returns the string with upper chars and an exclamation mark"""
    new_str = str1.upper() + "!"
    return new_str


def concat_list(list1, list2, list3):
    """Concatenate three lists into one."""
    return list1 + list2 + list3




